-- 6. ROW-LEVEL SECURITY (RLS) UNTUK TABEL LAPORAN
-- =================================================================
ALTER TABLE public.laporan ENABLE ROW LEVEL SECURITY;

-- Helper function untuk mendapatkan wilayah_id admin yang sedang login
CREATE OR REPLACE FUNCTION get_admin_wilayah_id()
RETURNS BIGINT AS $$
DECLARE
    wilayah_id_val BIGINT;
BEGIN
    SELECT wilayah_id INTO wilayah_id_val FROM public.admin WHERE id = auth.uid();
    RETURN wilayah_id_val;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Policy: Admin hanya bisa SELECT laporan dari wilayahnya.
CREATE POLICY "Admin can view reports from their own wilayah" ON public.laporan
FOR SELECT
TO authenticated
USING (wilayah_id = get_admin_wilayah_id());

-- Policy: Admin hanya bisa UPDATE laporan dari wilayahnya.
CREATE POLICY "Admin can update reports from their own wilayah" ON public.laporan
FOR UPDATE
TO authenticated
USING (wilayah_id = get_admin_wilayah_id());

-- Policy: User publik bisa INSERT laporan jika menyetujui disclaimer.
CREATE POLICY "Public can insert reports with disclaimer" ON public.laporan
FOR INSERT
WITH CHECK (disclaimer_agreed = true);

-- Policy: User publik bisa SELECT semua laporan (filtering di sisi client).
CREATE POLICY "Public can view all reports" ON public.laporan
FOR SELECT
USING (true);


